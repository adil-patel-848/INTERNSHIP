// 5) Write a program to find sum of two integers for function with arguments and without returning values. 

class Q5
{
    public static void main(String[]args)
    {
        int a=8,b=7;
        getSum(a,b);
    }
    static void getSum(int x,int y)
    {
        System.out.println("Sum: "+ (x+y));
    }
}