// 2) Write a program find the sum of N numbers using Array. 

class Q2
{
    public static void main(String[]args)
    {
        int a[]={12,34,56,78,90,87};
        int sum=0;
        for(int i=0;i<a.length;i++)
        {
            sum=sum+a[i];
        }
        System.out.println("Sum :"+sum);;

    
    }
}